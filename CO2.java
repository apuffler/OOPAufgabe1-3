public class CO2{
    private int bound;
    private int released;

    public int getBoundCO2(){
        return this.bound;
    }

    public int getReleasedCO2(){
        return this.released;
    }

    public void setBoundCO2(int boundCO2){
        this.bound = boundCO2;
    }

    public void setReleasedCO2(int releasedCO2){
        this.released = releasedCO2;
    }
}